#include<bits/stdc++.h>
using namespace std;

int main(){
	cout << "Toan tu ++a: (Tang roi moi dung):\n";
	int a = 1;
	int b = ++a;
	cout << "a = " << a;
	cout << "\nb = " << b;
	
	//++ sau: dung r moi tang
	cout << "\n\nToan tu a++: (Dung r moi tang):\n";
	int c = 3;
	int d = c++;
	cout << "c = " << c;
	cout << "\nd = " << d;
}
